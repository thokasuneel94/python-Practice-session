# identifiers :
# a name in a python program is called identifier.
# this name can be, class name, method name, variable name, function name, package name, module name.

# rules to define identifiers in python :
# the only allowed characters to write identifier in python are :
# alphabets, these can be either lower case or upper case(a-z, A-Z).
# digits(0-9).
# underscore symbol(_).
# example
cash=10
print(cash)       # output is 10
# ca$h=10
# print(ca$h)       # output is syntaxerror
# identifier should not start with digit.
# example
total123=10
print(total123)   # output is 10
# 123total=10
# print(123total)   # output is syntaxerror
# identifiers are case sensitive.
# example
total=10
print(total)          # output is 10
TOTAL=20
print(TOTAL)          # output is 20
# example
a=10
b=20
print(a+b)
# print(A+B)            # output is nameerror
# we can not use keywords as indentifiers.
# example
# x=10
# print(x)       # output is 10
# def=20
# print(def)     # output is syntaxerror
# if=30
# print(if)      # output is syntaxerror
# spaces are not allowed between identifiers.
# name id=10
# print(name id)        # output is syntaxerror
name_id=10
print(name_id)        # output is 10






