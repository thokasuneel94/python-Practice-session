# variables :
# variable is a reserved  memory location to store values.
# variables in python can be declared by any name or even alphabets like a, aa, abc, etc.

# rules for creating variable in  python:

# variable name can only contain alpha-numeric characters and underscores ( A-Z, a-z, 0-9, _ )
# variable name must start with a letter or the underscore character.
# variable name can not start with a number.
# variable name are case-sensitive ( name, Name and NAME are three different variables ).
# the reserved words(keywords) can not be used naming the variables.

