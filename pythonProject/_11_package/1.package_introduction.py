# packages concepts:

# package is a group of related items(items means modules)
# all those modules into a single folder is called package
# it is an encapsulation mechanism to group related modules into a single unit
# in package __init__.py file is must be there
# __init__.py file:
# python 3.3 onwards __init__.py file is not mendatory. before python 3.3 __init__.py file is mendatory
# a package can contain sub packages also
# a group of related modules into a single unit is called package
# if any folder considered as a package.compulsory that folder contain __init__.py file

# advantages:

# we can resolve naming conflicts
# we can identify our components uniquely
# modularity improved
# readability and maintanability
















